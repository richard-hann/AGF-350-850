
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% ----------------------- CALCULATING POTENTIAL TEMPERATURE ----------------------------%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

        function TP = calc_pot_temp(T,P)

    % Inputs: 
        % T = temperature in Celcius
        % P = pressure in hPa

            TP = (273.15 + T(:)).*(1000./P(:)).^.287 - 273.15;

